// Iterar con While Loops.

var myArray = [];

var i = 0;
while(i < 5) {
	myArray.push(i);
	i++;
}

//console.log(myArray);

// Iterar con For Loops.

 var ourArray = [];

 for (var i = 0; i < 5; i++) {
 	ourArray.push(i);
 }

//console.log(ourArray);

// Iterar numeros pares e impares con un For Loop.

 var parArray = [];

 for (var i = 0; i < 10; i += 2) {
 	parArray.push(i);
 }

 //console.log(parArray);

